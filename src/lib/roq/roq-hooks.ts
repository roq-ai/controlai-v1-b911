export function useAttackFindMany<
  T extends Prisma.attackFindManyArgs,
  R extends GetFindResult<Prisma.$attackPayload<DefaultArgs>, T>[],
>(
  args?: Prisma.SelectSubset<T, Prisma.attackFindManyArgs>,
  options?: RequestOptions,
  swrOptions?: SWRRequestOptions<R>,
) {
  const roq = useRoqClient();
  const key = JSON.stringify(['useAttackFindMany', args || {}]);
  return useSWR<R, Error>(
    key,
    async () => {
      const result = await roq.attack.findMany(args, options);
      return result as R;
    },
    {
      ...swrOptions,
      fallbackData: swrOptions?.initialData ?? swrOptions?.fallbackData,
    },
  );
}

